const dropdowns = document.querySelectorAll(".select-container");
const fromCurrency = dropdowns[0];
const toCurrency = dropdowns[1];
const fromImg = document.querySelectorAll("img")[0];
const toImg = document.querySelectorAll("img")[1];
const exchangeBtn = document.querySelector("button");
const inputField = document.querySelector("#input-field");
const rateText = document.querySelector("#exchange-rate");

// Populate currency dropdowns
for (let select of dropdowns) {
  for (let currCode in countryList) {
    let option = document.createElement("option");
    option.value = currCode;
    option.innerText = currCode;
    // Set default selected
    if (select === fromCurrency && currCode === "USD") option.selected = true;
    if (select === toCurrency && currCode === "BDT") option.selected = true;
    select.appendChild(option);
  }

  // Update flag when changed
  select.addEventListener("change", (e) => {
    updateFlag(e.target);
  });
}

const updateFlag = (element) => {
  let currencyCode = element.value;
  let countryCode = countryList[currencyCode];
  let imgTag = element.parentElement.querySelector("img");
  imgTag.src = `https://flagsapi.com/${countryCode}/flat/64.png`;
}

exchangeBtn.addEventListener("click", () => {
  let amount = parseFloat(inputField.value.trim());
  inputField.value = '';
  if (isNaN(amount) || amount <= 0) {
    rateText.innerText = "Please enter a valid amount";
    rateText.style.backgroundColor = 'red';
    return;
  }

  let fromCurr = fromCurrency.value;
  let toCurr = toCurrency.value;

  const url = `https://api.exchangerate.host/latest?base=${fromCurr}&symbols=${toCurr}`;

  fetch(url)
    .then(res => res.json())
    .then(data => {
        console.log(data)
      const rate = data.rates[toCurr];
      const converted = (amount * rate).toFixed(2);
      rateText.innerText = `${amount} ${fromCurr} = ${converted} ${toCurr}`;
      rateText.style.backgroundColor = 'green';
    })
    .catch(() => {
      rateText.innerText = "Something went wrong ðŸ˜¢";
      rateText.style.backgroundColor = 'red'
    });
});
